const mongoose = require('mongoose');

const usuarioSchema = new mongoose.Schema({
    nombre: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, {
    timestamps: true  // Esto añade automáticamente createdAt y updatedAt
});

module.exports = mongoose.model('Usuario', usuarioSchema);